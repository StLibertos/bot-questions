# Ініціалізація бота
intents = discord.Intents.default()
intents.messages = True

class MyBot(discord.Client):
    def __init__(self):
        super().__init__(intents=intents)
        self.tree = discord.app_commands.CommandTree(self)

    async def on_ready(self):
        await self.tree.sync()  # Синхронізація команд з Discord
        print(f'Бот {self.user} готовий!')

# Функція для відправки випадкового запитання
async def send_random_question(interaction: discord.Interaction):
    question = random.choice(questions)
    print(f'Запитання: {question}')  # Логування для перевірки, що запитання вибране
    await interaction.response.send_message(question)

# Створюємо екземпляр бота
bot = MyBot()

# Додаємо слеш-команду для глибокого питання
@bot.tree.command(name="deepq", description="Задає випадкове глибоке запитання")
async def deep_question(interaction: discord.Interaction):
    print(f'Команду отримано від {interaction.user}')  # Логування для перевірки команди
    await send_random_question(interaction)

